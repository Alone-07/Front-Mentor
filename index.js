function myToggle1() {
  const hide = document.getElementById("open1");
  const arrow = document.getElementById("one");
  // const shows = document.querySelectorAll("p.text_show_part");
  const shows = document.getElementById("show1");

  if (hide.style.display == "none") {
    hide.style.display = "block";
    arrow.style.transform = "rotate(180deg)";
    shows.style.fontWeight = "700";
  } else {
    hide.style.display = "none";
    arrow.style.transform = "rotate(0deg)";
  }
}
